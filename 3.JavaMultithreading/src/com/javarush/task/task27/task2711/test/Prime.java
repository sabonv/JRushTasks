package com.javarush.task.task27.task2711.test;

public class Prime {

    private String name;
    private int cost;
    private int weight;

    public Prime(String name, int cost, int weight) {
        this.name = name;
        this.cost = cost;
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public int getCost() {
        return cost;
    }

    public int getWeight() {
        return weight;
    }

}
